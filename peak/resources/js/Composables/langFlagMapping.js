// Get the flag for a given language code
export function getFlagByLanguageCode(langCode) {
    const countryCode = languageToCountryMap[langCode] || langCode.toUpperCase();
    return emojiFlags[countryCode]?.emoji || '🏳️'; // Fallback to a default flag if not found
}

// You can also export all the language mappings if needed
export function getAllLangFlags() {
    return Object.keys(languageToCountryMap).map((langCode) => {
        return {
            code: langCode,
            flag: getFlagByLanguageCode(langCode),
        };
    });
}
